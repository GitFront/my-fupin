package com.aspire.birp.modules.antiPoverty.model.poorFile.countryFileNews;

import java.util.List;

import org.apache.ibatis.type.Alias;

@Alias("CountryFileBase")
public class CountryFileBase {
	private String idNew;
	private String timeNew;
	private String descNew;
	private List pics;
	
	public String getIdNew() {
		return idNew;
	}

	public void setIdNew(String idNew) {
		this.idNew = idNew;
	}

	public String getTimeNew() {
		return timeNew;
	}

	public void setTimeNew(String timeNew) {
		this.timeNew = timeNew;
	}

	public String getDescNew() {
		return descNew;
	}

	public void setDescNew(String descNew) {
		this.descNew = descNew;
	}

	public List getPics() {
		return pics;
	}

	public void setPics(List pics) {
		this.pics = pics;
	}

	@Override
	public String toString() {
		
		return "{\"id\":\"" + idNew + "\",\"time\":\"" + timeNew + "\",\"desc\":\""
				+ descNew + "\",\"pics\":" + pics + "}  ";
	}
}
