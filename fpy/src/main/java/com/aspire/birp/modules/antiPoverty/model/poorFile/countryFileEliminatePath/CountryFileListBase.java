package com.aspire.birp.modules.antiPoverty.model.poorFile.countryFileEliminatePath;

import org.apache.ibatis.type.Alias;

@Alias("CountryFileListBase")
public class CountryFileListBase {
	private String nameNew;
	private String groupNew;
	private String valueNew;
	public String getnameNew() {
		return nameNew;
	}
	public void setnameNew(String nameNew) {
		this.nameNew = nameNew;
	}
	public String getgroupNew() {
		return groupNew;
	}
	public void setgroupNew(String groupNew) {
		this.groupNew = groupNew;
	}
	public String getvalueNew() {
		return valueNew;
	}
	public void setvalueNew(String valueNew) {
		this.valueNew = valueNew;
	}
	@Override
	public String toString() {
		return "{\"name\":\"" + nameNew + "\",\"group\":\"" + groupNew
				+ "\",\"value\":\"" + valueNew + "\"}  ";
	}

}
