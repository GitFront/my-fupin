package com.aspire.birp.modules.antiPoverty.model.poorFile.countryFileImplement;

import org.apache.ibatis.type.Alias;

@Alias("CountryFileImpBase")
public class CountryFileImpBase {
	private String type1;
	private String nameNew;
	private String size1;
	private String investExpected;
	private String investActual;
	private String profit;
	private String timeStart;
	private String timeCompleted;
	private String projId;
	public String getTimeCompleted() {
		return timeCompleted;
	}
	public void setTimeCompleted(String timeCompleted) {
		this.timeCompleted = timeCompleted;
	}
	public String getProjId() {
		return projId;
	}
	public void setProjId(String projId) {
		this.projId = projId;
	}
	public String gettype1() {
		return type1;
	}
	public void settype1(String type1) {
		this.type1 = type1;
	}
	public String getnameNew() {
		return nameNew;
	}
	public void setnameNew(String nameNew) {
		this.nameNew = nameNew;
	}
	public String getsize1() {
		return size1;
	}
	public void setsize1(String size1) {
		this.size1 = size1;
	}
	public String getInvestExpected() {
		return investExpected;
	}
	public void setInvestExpected(String investExpected) {
		this.investExpected = investExpected;
	}
	public String getInvestActual() {
		return investActual;
	}
	public void setInvestActual(String investActual) {
		this.investActual = investActual;
	}
	public String getProfit() {
		return profit;
	}
	public void setProfit(String profit) {
		this.profit = profit;
	}
	public String getTimeStart() {
		return timeStart;
	}
	public void setTimeStart(String timeStart) {
		this.timeStart = timeStart;
	}
	
	@Override
	public String toString() {
		
		
		return "{\"type\":\"" + type1 + "\",\"name\":\"" + nameNew
				+ "\",\"size\":\"" + size1 + "\",\"investExpected\":\""
				+ investExpected + "\",\"investActual\":\"" + investActual
				+ "\",\"profit\":\"" + profit 
				+ "\",\"timeStart\":\""+ timeStart 
				+ "\",\"timeCompleted\":\""+ timeCompleted 
				+ "\",\"album\":" + projId + "}";
	}
	
	
}
