
package com.david.vo;

import java.util.HashMap;
import java.util.Map;

/**  
 * @Title: 地图GEO数据区域对象 
 * @Description: 用于生成地图GEO数据区域对象 
 * @Copyright: aspire Copyright (C) 2009
 * @version: V1.0
 */
public class MapGeoFeature {

	private String type = "Feature";
	private Map<String,Object> properties;
	private MapGeoGeometry geometry;
	
	public MapGeoFeature() {
		properties = new HashMap<String, Object>();
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Map<String, Object> getProperties() {
		return properties;
	}

	public void setProperties(Map<String, Object> properties) {
		this.properties = properties;
	}

	public MapGeoGeometry getGeometry() {
		return geometry;
	}

	public void setGeometry(MapGeoGeometry geometry) {
		this.geometry = geometry;
	}
	
	
}


