public class SqDataGridObject {

	private String field;
	private String halign = "center";
	private String align;
	private String title;
	private String width;
	private boolean hidden;
	private boolean sortable;
	
	
	public SqDataGridObject() {
		super();
	}
	
	public SqDataGridObject(String field, String title) {
		super();
		this.field = field;
		this.title = title;
	}

	public SqDataGridObject(String field, boolean hidden) {
		super();
		this.field = field;
		this.hidden = hidden;
	}


	public SqDataGridObject(String field, String title, String align) {
		super();
		this.field = field;
		this.align = align;
		this.title = title;
	}


	public String getField() {
		return field;
	}
	public void setField(String field) {
		this.field = field;
	}
	public String getHalign() {
		return halign;
	}
	public void setHalign(String halign) {
		this.halign = halign;
	}
	public String getAlign() {
		return align;
	}
	public void setAlign(String align) {
		this.align = align;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getWidth() {
		return width;
	}
	public void setWidth(String width) {
		this.width = width;
	}
	public boolean isHidden() {
		return hidden;
	}
	public void setHidden(boolean hidden) {
		this.hidden = hidden;
	}
	public boolean isSortable() {
		return sortable;
	}
	public void setSortable(boolean sortable) {
		this.sortable = sortable;
	}
	
	
	
	
}


