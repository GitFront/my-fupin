public class DmDatabaseColumns {
	// Fields
	/*映射表ID*/
	private String mpTableId;
	/*映射表名*/
	private String mpTableName;
	/*保存的column列表*/
	private List<DmDatabaseColumn> columns;

	// Constructors

	/** default constructor */
	public DmDatabaseColumns() {
	}

	public DmDatabaseColumns(String mpTableId, String mpTableName,
			List<DmDatabaseColumn> columns) {
		super();
		this.mpTableId = mpTableId;
		this.mpTableName = mpTableName;
		this.columns = columns;
	}



	public String getMpTableId() {
		return mpTableId;
	}

	public void setMpTableId(String mpTableId) {
		this.mpTableId = mpTableId;
	}

	public String getMpTableName() {
		return mpTableName;
	}

	public void setMpTableName(String mpTableName) {
		this.mpTableName = mpTableName;
	}

	public List<DmDatabaseColumn> getColumns() {
		return columns;
	}

	public void setColumns(List<DmDatabaseColumn> columns) {
		this.columns = columns;
	}

	

}