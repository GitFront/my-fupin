public class InvertedLineVo {
	/**
	 * 纵轴的数值
	 */
	private double y;
	/**
	 * 分组数据
	 */
	private String group;
	/**
	 * 横轴的数值
	 */
	private double x;
	public InvertedLineVo(double y, String group, double x) {
		super();
		this.y = y;
		this.group = group;
		this.x = x;
	}
	public InvertedLineVo() {
		super();
		// TODO Auto-generated constructor stub
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	public String getGroup() {
		return group;
	}
	public void setGroup(String group) {
		this.group = group;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	

}
